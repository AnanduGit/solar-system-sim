// ELP2000 geocentric Moon position (equatorial J2000)
function moonState_pos(T) {
  const wrap = deg => ((deg % 360) + 360) % 360;
  const D  = wrap(297.85036 + 445267.111480*T - 0.0019142*T*T) * DEG;
  const MS = wrap(357.52772 +  35999.050340*T - 0.0001603*T*T) * DEG;
  const MM = wrap(134.96298 + 477198.867398*T + 0.0086972*T*T) * DEG;
  const F  = wrap( 93.27191 + 483202.017538*T - 0.0036825*T*T) * DEG;
  const O  = wrap(125.04452 -   1934.136261*T + 0.0020708*T*T) * DEG;
  let dLon=0;
  for(const [d,ms,mm,f,c] of ELP_LON) dLon += c*Math.sin(d*D+ms*MS+mm*MM+f*F);
  dLon += -17.2e6*Math.sin(O) - 1.32e6*Math.sin(2*(wrap(218.3165+481267.8813*T)*DEG))
          - 0.23e6*Math.sin(2*MM) + 0.21e6*Math.sin(2*O);
  dLon /= 1e6;
  const lon = (wrap(218.3165 + 481267.8813*T) + dLon) * DEG;
  let dDist=0;
  for(const [d,ms,mm,f,c] of ELP_DIST) dDist += c*Math.cos(d*D+ms*MS+mm*MM+f*F);
  const r_km = 385000.56 + dDist/1000;
  const r_au = r_km / 1.496e8;
  let dLat=0;
  for(const [d,ms,mm,f,c] of ELP_LAT) dLat += c*Math.sin(d*D+ms*MS+mm*MM+f*F);
  const lat = (dLat/1e6) * DEG;
  const cB=Math.cos(lat), sB=Math.sin(lat), cL=Math.cos(lon), sL=Math.sin(lon);
  const xEc = r_au*cB*cL, yEc = r_au*cB*sL, zEc = r_au*sB;
  const eps=23.43929111*DEG, ce=Math.cos(eps), se=Math.sin(eps);
  return { x:xEc, y:yEc*ce-zEc*se, z:yEc*se+zEc*ce, r_au, O };
}

// moonState: ELP2000 position + vis-viva velocity.
//
// WHY NOT FINITE DIFFERENCES:
// The truncated ELP2000 series predicts angular velocity ~14% too low at
// some orbital phases. Specifically at early 2026 the series gives v/v_c~0.87
// while the correct vis-viva answer is v/v_c~1.03.
//
// The fix: ELP2000 gives accurate position and radius; vis-viva gives accurate
// speed (v^2 = GM*(2/r - 1/a), depends only on r and a, not on series velocity);
// the orbital plane normal (from mean inclination + ascending node) gives the
// prograde direction. This combination is exact for a Keplerian orbit and
// accurate to ~1% for the actual perturbed Moon.
function moonState(T) {
  const pos = moonState_pos(T);
  const GM_e   = GM * 3.0035e-6;       // AU^3/day^2
  const a_mean = 384400 / 1.496e8;     // mean semi-major axis [AU]

  // vis-viva orbital speed (accurate regardless of true anomaly)
  const v_vv = Math.sqrt(GM_e * (2/pos.r_au - 1/a_mean));

  // Orbital plane normal in ecliptic frame: n = (sin(i)*sin(O), -sin(i)*cos(O), cos(i))
  const i = 5.145 * DEG;
  const nEx = Math.sin(i)*Math.sin(pos.O);
  const nEy = -Math.sin(i)*Math.cos(pos.O);
  const nEz = Math.cos(i);
  // Rotate orbital plane normal from ecliptic to equatorial
  const eps=23.43929111*DEG, ce=Math.cos(eps), se=Math.sin(eps);
  const nx = nEx;
  const ny = nEy*ce - nEz*se;
  const nz = nEy*se + nEz*ce;

  // Prograde direction = n x r_hat (perpendicular to r, in orbital plane, prograde)
  const r = pos.r_au;
  const rx=pos.x/r, ry=pos.y/r, rz=pos.z/r;
  let px = ny*rz - nz*ry;
  let py = nz*rx - nx*rz;
  let pz = nx*ry - ny*rx;
  const pm = Math.sqrt(px*px + py*py + pz*pz);
  px /= pm; py /= pm; pz /= pm;

  return {
    x: pos.x, y: pos.y, z: pos.z,
    vx: v_vv*px, vy: v_vv*py, vz: v_vv*pz,
  };
}

// Kepler equation solver — used for dwarfs and moon init
function solveKepler(M,e){
  let E=M+e*Math.sin(M);
  for(let i=0;i<80;i++){const dE=(M-E+e*Math.sin(E))/(1-e*Math.cos(E));E+=dE;if(Math.abs(dE)<1e-13)break;}
  return E;
}

