      // ── Rankine vortex warp ───────────────────────────────────────
      function vortexWarp(px, py, cx, cy, rx, ry, str){
        const dx=(px-cx)/rx, dy=(py-cy)/ry;
        const d2=dx*dx+dy*dy; if(d2>6.25) return [0,0];
        const d=Math.sqrt(d2)+1e-5;
        const v=d<1?d:1/d;
        const ang=Math.atan2(dy,dx)+Math.PI/2;
        const fall=Math.exp(-d2*0.4)*v;
        return [Math.cos(ang)*fall*str, Math.sin(ang)*fall*str*0.5];
      }

      // Storm features
      const GRS   = {cx:0.61, cy:0.610, rx:0.075, ry:0.048, str:0.055};
