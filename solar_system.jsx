// Evaluate VSOP87 series for a planet at time T (Julian centuries from J2000)
// Returns heliocentric ecliptic {L(rad), B(rad), R(AU)}
// Evaluate VSOP87 series + analytic derivatives at Julian centuries T.
// Returns position {L,B,R} in radians/AU and derivatives {dL,dB,dR} in rad/day & AU/day.
function vsopLBR(name, T) {
  const tau = T / 10;                    // Julian millennia
  const dtau = 1.0 / (10.0 * 36525.0);  // dtau/dt [1/day]
  const p = VSOP[name];
  if (!p) return null;

  // L: [[L0 terms],[L1 terms]] → L = L0 + L1·τ
  // dL/dτ = dL0/dτ + dL1/dτ·τ + L1  (product rule on L1·τ)
  let L=0, dL_dtau=0;
  for(let k=0; k<p.L.length; k++){
    let sumC=0, sumS=0;
    for(const [A,Barg,C] of p.L[k]){
      const ang = Barg + C*tau;
      sumC +=  A * Math.cos(ang);
      sumS += -A * C * Math.sin(ang);  // d/dtau of A·cos(B+C·τ)
    }
    const tauK = Math.pow(tau, k);
    L        += sumC * tauK * 1e-8;
    dL_dtau  += (sumS * tauK + (k > 0 ? sumC * k * Math.pow(tau, k-1) : 0)) * 1e-8;
  }

  // B and R: single level (no tau power)
  let B=0, dB_dtau=0, R=0, dR_dtau=0;
  for(const [A,Barg,C] of p.B){ const ang=Barg+C*tau; B+=A*Math.cos(ang)*1e-8; dB_dtau+=-A*C*Math.sin(ang)*1e-8; }
  for(const [A,Barg,C] of p.R){ const ang=Barg+C*tau; R+=A*Math.cos(ang)*1e-8; dR_dtau+=-A*C*Math.sin(ang)*1e-8; }

  return { L, B, R, dL: dL_dtau*dtau, dB: dB_dtau*dtau, dR: dR_dtau*dtau };
}

// Convert VSOP87 heliocentric ecliptic (L,B,R) + analytic derivatives to cartesian state.
function vsopState(name, T) {
  const s = vsopLBR(name, T);
  if (!s) return null;
  const { L, B, R, dL, dB, dR } = s;
  const cB=Math.cos(B), sB=Math.sin(B), cL=Math.cos(L), sL=Math.sin(L);
  const x  =  R*cB*cL,          y  =  R*cB*sL,          z  =  R*sB;
  // Analytic velocity — exact derivative of (x,y,z) w.r.t. time
  const vx = dR*cB*cL - R*sB*cL*dB - R*cB*sL*dL;
  const vy = dR*cB*sL - R*sB*sL*dB + R*cB*cL*dL;
  const vz = dR*sB              + R*cB    *dB;
  return { x, y, z, vx, vy, vz };
}

function planetState(p,T){
  // Use VSOP87 for the 8 main planets; fall back to Keplerian for dwarfs
  const vs = vsopState(p.n, T);
  if(vs) return vs;
  const a=p.a0+p.da*T,e=p.e0+p.de*T;
  const iR=(p.i0+p.di*T)*DEG,LR=(p.L0+p.dL*T)*DEG,wR=(p.w0+p.dw*T)*DEG,OR=(p.O0+p.dO*T)*DEG;
  const om=wR-OR,M=((LR-wR)%(2*Math.PI)+2*Math.PI)%(2*Math.PI);
  const E=solveKepler(M,e),nu=2*Math.atan2(Math.sqrt(1+e)*Math.sin(E/2),Math.sqrt(1-e)*Math.cos(E/2));
  const r=a*(1-e*Math.cos(E)),xO=r*Math.cos(nu),yO=r*Math.sin(nu);
  const h=Math.sqrt(GM*a*(1-e*e)),vxO=-(GM/h)*Math.sin(nu),vyO=(GM/h)*(e+Math.cos(nu));
  const cO=Math.cos(OR),sO=Math.sin(OR),ci=Math.cos(iR),si=Math.sin(iR),cw=Math.cos(om),sw=Math.sin(om);
  const Px=cO*cw-sO*sw*ci,Py=sO*cw+cO*sw*ci,Pz=sw*si;
  const Qx=-cO*sw-sO*cw*ci,Qy=-sO*sw+cO*cw*ci,Qz=cw*si;
  return{x:Px*xO+Qx*yO,y:Py*xO+Qy*yO,z:Pz*xO+Qz*yO,vx:Px*vxO+Qx*vyO,vy:Py*vxO+Qy*vyO,vz:Pz*vxO+Qz*vyO};
}


// Ecliptic → Three.js scene coordinates (Y-up, ecliptic north = scene Y)
// Keplerian moon initializer — places a moon in orbit around its parent body
function moonInitState(m, parentBody, T_days=0) {
  const aAU=m.a, e=m.e;
  // Apply secular precession rates if defined (deg/day from J2000)
  const O_epoch = m.O + (m.dO||0) * T_days;
  const w_epoch = m.w + (m.dw||0) * T_days;
  const iR=m.inc*DEG, OR=O_epoch*DEG, wR=w_epoch*DEG;
  const MR=((m.M0*DEG)%(2*Math.PI)+2*Math.PI)%(2*Math.PI);
  const E=solveKepler(MR,e);
  const nu=2*Math.atan2(Math.sqrt(1+e)*Math.sin(E/2),Math.sqrt(1-e)*Math.cos(E/2));
  const r=aAU*(1-e*Math.cos(E)), xO=r*Math.cos(nu), yO=r*Math.sin(nu);
  // GMparent in AU³/day² — same units as GM (Sun). parentBody.mass is in Sun-mass units.
  const GMparent = GM * parentBody.mass;
  const h = Math.sqrt(GMparent * aAU * (1 - e*e));
  const vxO = -(GMparent/h)*Math.sin(nu), vyO = (GMparent/h)*(e+Math.cos(nu));
  const cO=Math.cos(OR),sO=Math.sin(OR),ci=Math.cos(iR),si=Math.sin(iR),cw=Math.cos(wR),sw=Math.sin(wR);
  const Px=cO*cw-sO*sw*ci,Py=sO*cw+cO*sw*ci,Pz=sw*si;
  const Qx=-cO*sw-sO*cw*ci,Qy=-sO*sw+cO*cw*ci,Qz=cw*si;
  return {
    x: parentBody.x + Px*xO+Qx*yO,
    y: parentBody.y + Py*xO+Qy*yO,
    z: parentBody.z + Pz*xO+Qz*yO,
    vx: parentBody.vx + Px*vxO+Qx*vyO,
    vy: parentBody.vy + Py*vxO+Qy*vyO,
    vz: parentBody.vz + Pz*vxO+Qz*vyO,
  };
}

// Build initial bodies array. Sun position/velocity derived from barycenter constraint:
// Σ m·r = 0, Σ m·v = 0 (solar system barycenter = inertial origin)
function initBodies(T) {
  const planetBodies = PLANETS.map(p => ({ n:p.n, mass:p.mass, ...planetState(p,T) }));
  const dwarfBodies  = DWARFS.map(p  => ({ n:p.n, mass:p.mass, ...planetState(p,T) }));
  // Moon via ELP2000
  const earth = planetBodies.find(b=>b.n==='Earth');
  const ms    = moonState(T);
  const moonBody = {
    n:'Moon', mass:3.694e-8,
    x:earth.x+ms.x, y:earth.y+ms.y, z:earth.z+ms.z,
    vx:earth.vx+ms.vx, vy:earth.vy+ms.vy, vz:earth.vz+ms.vz,
  };
  // All other moons via Keplerian elements
  const allBodiesMap = {};
  [...planetBodies, ...dwarfBodies].forEach(b => allBodiesMap[b.n] = b);
  const otherMoonBodies = MOONS.filter(m=>m.n!=='Moon').map(m => {
    const parent = allBodiesMap[m.parent];
    return { n:m.n, mass:m.mass, ...moonInitState(m, parent, T*36525) };
  });
  const smallBodies = SMALL_BODIES.map(p => ({ n:p.n, mass:0, ...planetState(p,T) }));
  const allButSun = [...planetBodies, ...dwarfBodies, moonBody, ...otherMoonBodies, ...smallBodies];
  // Barycenter constraint: Sun position/velocity so Σ m·r = 0, Σ m·v = 0
  let sx=0,sy=0,sz=0,svx=0,svy=0,svz=0;
  for(const b of allButSun){
    sx  -= b.mass*b.x;  sy  -= b.mass*b.y;  sz  -= b.mass*b.z;
    svx -= b.mass*b.vx; svy -= b.mass*b.vy; svz -= b.mass*b.vz;
  }
  const SUN_MASS = 1.0;
  const sunBody = { n:'Sun', mass:SUN_MASS, x:sx/SUN_MASS, y:sy/SUN_MASS, z:sz/SUN_MASS,
                    vx:svx/SUN_MASS, vy:svy/SUN_MASS, vz:svz/SUN_MASS };
  return [sunBody, ...allButSun];
}

// ── J2 oblateness parameters ─────────────────────────────────────────────────
// J2: dimensionless zonal harmonic coefficient
// Re: equatorial radius in AU (NOT scene units) — physics runs in AU, not scene units
//     Re = rv_scene / AU = real_km / 149,597,870
//     e.g. Earth: rv_scene=0.00426 → Re=0.00426/100=4.263e-5 AU  ← correct
// pole: rotation pole unit vector in ecliptic J2000 coords [x, y, z]
//   Ecliptic north = (0,0,1). Pole tilted by obliquity toward λ=270° (−y direction).
const J2_PARAMS = {
  Earth:   { J2: 1.08263e-3, Re: 4.263e-5, pole: [0, -0.3978, 0.9174] }, // ε=23.44°
  Mars:    { J2: 1.96045e-3, Re: 2.266e-5, pole: [0, -0.4253, 0.9050] }, // ε=25.19°
  Jupiter: { J2: 1.47360e-2, Re: 4.779e-4, pole: [0, -0.0546, 0.9985] }, // ε=3.13°
  Saturn:  { J2: 1.62980e-2, Re: 4.029e-4, pole: [0, -0.4493, 0.8934] }, // ε=26.73°
};
// Precompute Re² for speed
Object.values(J2_PARAMS).forEach(p => p.Re2 = p.Re * p.Re);

// ── PHYSICS COORDINATE SYSTEM ────────────────────────────────────────────────
// All physics (positions, velocities, GM) runs in AU and days.
// x,y,z in bodies[] are in AU (heliocentric ecliptic J2000).
// Velocities vx,vy,vz are AU/day.
// GM = 2.9591e-4 AU³/day² (heliocentric gravitational constant).
// mass is fraction of Sun mass (Sun=1.0, Earth=3.0035e-6, etc.)
// Conversion to scene: call e2t(x,y,z) → multiplies by AU=100 and swaps y↔z.
// NEVER mix scene units and AU in physics calculations.
function nbAccel(bodies){
  return bodies.map((bi,i)=>{
    let ax=0,ay=0,az=0;
    for(let j=0;j<bodies.length;j++){
      if(i===j) continue;
      const bj=bodies[j];
      const dx=bj.x-bi.x, dy=bj.y-bi.y, dz=bj.z-bi.z;
      const r2=dx*dx+dy*dy+dz*dz;
      // Single r check guards ALL three force terms below.
      // GR and J2 both divide by powers of r — without this they blow up
      // at small separations, producing Infinity that spreads to every body.
      if(r2 < 1e-10) continue;
      const r=Math.sqrt(r2);
      const gm=GM*bj.mass/(r2*r);
      ax+=gm*dx; ay+=gm*dy; az+=gm*dz;

      // ── 1PN Schwarzschild GR correction ────────────────────────────────────
      // Source: Newhall, Standish & Williams (1983) — same as JPL DE405.
      // Only applied to planet bi when bj is the Sun (test-particle approx).
      // r̂ = Sun→planet = (-dx,-dy,-dz)/r
      // v_rel = planet velocity relative to Sun
      // Δa = (GM☉/c²r³){[4GM☉/r − |v_rel|²]·r̂ + 4(r̂·v_rel)·v_rel}
      if(bj.n === 'Sun'){
        const GM_sun = GM * bj.mass;
        const rvx = bi.vx - bj.vx,  rvy = bi.vy - bj.vy,  rvz = bi.vz - bj.vz;
        const v2     = rvx*rvx + rvy*rvy + rvz*rvz;
        const rdotv  = (-dx)*rvx + (-dy)*rvy + (-dz)*rvz;   // r̂·v_rel (unnorm; /r later)
        const factor = GM_sun / (C2 * r2 * r);
        const A      = 4*(GM_sun/r) - v2;
        ax += factor * (A*(-dx) + 4*rdotv*rvx);
        ay += factor * (A*(-dy) + 4*rdotv*rvy);
        az += factor * (A*(-dz) + 4*rdotv*rvz);
      }

      // ── J2 oblateness perturbation ────────────────────────────────────────
      // Applied when bj is an oblate body (Earth/Mars/Jupiter/Saturn) and bi
      // is a moon orbiting it. Uses Legendre P2 zonal harmonic.
      // a_J2 = (3/2)*J2*GM_j*Re²/r⁵ * {[5(ẑ·r̂)²−1]·ρ − 2(ẑ·ρ)·ẑ}
      // where ẑ is bj's pole unit vector and ρ = r_bi − r_bj
      const jp = J2_PARAMS[bj.n];
      if(jp){
        // ρ = position of bi relative to bj (same as -[dx,dy,dz])
        const rx=-dx, ry=-dy, rz=-dz; // bi - bj
        const [px,py,pz] = jp.pole;
        const zdotr = px*rx + py*ry + pz*rz; // ẑ · ρ
        const r5 = r2*r2*r;
        const coef = 1.5 * jp.J2 * GM * bj.mass * jp.Re2 / r5;
        const f1 = 5*zdotr*zdotr/(r2) - 1;
        ax += coef * (f1*rx - 2*zdotr*px);
        ay += coef * (f1*ry - 2*zdotr*py);
        az += coef * (f1*rz - 2*zdotr*pz);
      }
    }
    return{ax,ay,az};
  });
}

// Yoshida 4th-order symplectic integrator
// Conserves a shadow Hamiltonian exactly — no long-term energy drift unlike RK4
const _cbrt2 = Math.cbrt(2);
const _w1 = 1 / (2 - _cbrt2);
const _w0 = -_cbrt2 * _w1;
const _YC = [_w1/2, (_w0+_w1)/2, (_w0+_w1)/2, _w1/2]; // position (drift) coefficients
const _YD = [_w1, _w0, _w1];                             // momentum (kick) coefficients

function yoshida(bodies, dt) {
  let bs = bodies;
  for (let i = 0; i < 3; i++) {
    const c = _YC[i] * dt;
    bs = bs.map(b => ({ ...b, x: b.x+b.vx*c, y: b.y+b.vy*c, z: b.z+b.vz*c }));
    const d = _YD[i] * dt;
    const a = nbAccel(bs);
    bs = bs.map((b,j) => ({ ...b, vx: b.vx+a[j].ax*d, vy: b.vy+a[j].ay*d, vz: b.vz+a[j].az*d }));
  }
  const c3 = _YC[3] * dt;
  bs = bs.map(b => ({ ...b, x: b.x+b.vx*c3, y: b.y+b.vy*c3, z: b.z+b.vz*c3 }));
  return bs;
}

